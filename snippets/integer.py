# An expression like 1/2 returns a float

print('3 / 2 =', 3 / 2)
# 3 / 2 = 1.5

print('3 // 2 =', 3 // 2)
# 3 // 2 = 1

print('3 / 2.0 =', 3 / 2.0)
# 3 / 2.0 = 1.5

print('3 // 2.0 =', 3 // 2.0)
# 3 // 2.0 = 1.0

